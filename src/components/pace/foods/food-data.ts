export function mealIcoFor(key: string): string {
  const k = key.toLowerCase();
  if (k.includes("shake") || k.includes("protein shake")) return "🥤";
  if (k.includes("pancake")) return "🥞";
  if (k.includes("bagel")) return "🥯";
  if (k.includes("burrito")) return "🌯";
  if (k.includes("halloumi") || k.includes("feta")) return "🧀";
  if (k.includes("chia")) return "🌱";
  if (k.includes("hash") || k.includes("skillet")) return "🍳";
  if (k.includes("scramble")) return "🍳";
  if (k.includes("oats") || k.includes("porridge")) return "🥣";
  if (k.includes("bacon")) return "🥓";
  if (k.includes("granola") || k.includes("parfait")) return "🥣";
  if (k.includes("almond butter")) return "🥜";
  if (k.includes("muffin")) return "🧁";
  if (k.includes("hummus")) return "🥕";
  if (k.includes("cottage")) return "🍍";
  if (k.includes("lentil") || k.includes("curry")) return "🍛";
  if (k.includes("chilli")) return "🌶️";
  if (k.includes("cod")) return "🐟";
  if (k.includes("quinoa")) return "🥗";
  if (k.includes("yogurt")) return "🥣";
  if (k.includes("egg")) return "🍳";
  if (k.includes("tofu")) return "🥦";
  if (k.includes("salmon")) return "🐟";
  if (k.includes("tuna")) return "🥪";
  if (k.includes("wrap")) return "🌯";
  return "🍗";
}

